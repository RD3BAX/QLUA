function OnInit()
   -- Получает доступ к свечам графика
   local Error = '';
   DS,Error = CreateDataSource(CLASS_CODE, SEC_CODE, INTERVAL);
   -- Проверка
   if DS == nil then
      message('Простой MA-робот:ОШИБКА получения доступа к свечам! '..Error);
      -- Завершает выполнение скрипта
      Run = false;
      return;
