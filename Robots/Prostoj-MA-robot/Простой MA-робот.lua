   message('Простой MA-робот: '..ROBOT_STATE);
   -- "Бесконечный" цикл
   while Run do 
      --Если СОСТОЯНИЕ робота "В ПРОЦЕССЕ СДЕЛКИ"
      if ROBOT_STATE == 'В ПРОЦЕССЕ СДЕЛКИ' then
         -- Выводит сообщение
